// A estrutura condicional if permite ao JavaScript executar um trecho de código somente se uma determinada condição for verdadeira.

/*
< menor
> maior
= = igual
> = maior ou igual
< = menor ou igual
! = diferente

Obs: espaços servem para olhar de uma forma ampla sem que o tema converta em um simbolo 
*/

let idade = 18

if (idade >= 18) {
  console.log('Você é maior de idade')
} else {
  console.log('Você é menor de idade')
}
