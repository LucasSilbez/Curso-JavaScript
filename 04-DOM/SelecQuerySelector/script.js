// querySelector é uma forma de selecionar um elemento conforme exemplo abaixo

document.querySelector('#exemplo') // seleciono o id ou # e class com ., similar ao CSS

// Obs: caso seja necessário selecionar uma class, ele irá verificar sempre a primeira class, caso eu queira selecionar todas para retornar um array, deve ser feito conforme exemplo abaixo

document.querySelectorAll('.lista')
